namespace Lw.Route.Routes.Mappings
{
    /// <summary>
    /// 匹配email结果
    /// </summary>
    [UrlNameMap("email")]
    class EmailMatch : RegexMappingBase
    {
        protected override string RegexString => @"^([\w-\.]+)@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.)|(([\w-]+\.)+))([a-zA-Z]{2,4}|[0-9]{1,3})(\]?)$";
    }
}